  // ─── Current language ───────────────────────────────────────────────────────
  window.currentLang = localStorage.getItem('thebar_lang') || 'cs';

  window.t = function (key) {
    var dict = window.translations[window.currentLang] || window.translations.cs;
    return dict[key] !== undefined ? dict[key] : (window.translations.cs[key] !== undefined ? window.translations.cs[key] : key);
  };

  window.applyLanguage = function (lang) {
    if (!window.translations[lang]) return;
    window.currentLang = lang;
    localStorage.setItem('thebar_lang', lang);
    document.documentElement.lang = lang;

    // text nodes
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var v = window.t(el.getAttribute('data-i18n'));
      if (v !== undefined) el.textContent = v;
    });

    // innerHTML (elements with em/br/strong)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var v = window.t(el.getAttribute('data-i18n-html'));
      if (v !== undefined) el.innerHTML = v;
    });

    // input/textarea placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var v = window.t(el.getAttribute('data-i18n-placeholder'));
      if (v !== undefined) el.placeholder = v;
    });

    // lang-switch active state
    var czEl = document.getElementById('lang-cz');
    var enEl = document.getElementById('lang-en');
    if (czEl && enEl) {
      czEl.style.color = ''; czEl.style.fontWeight = '';
      enEl.style.color = ''; enEl.style.fontWeight = '';
      if (lang === 'cs') {
        czEl.classList.add('lang-active'); enEl.classList.remove('lang-active');
        czEl.setAttribute('aria-current', 'true'); enEl.setAttribute('aria-current', 'false');
      } else {
        enEl.classList.add('lang-active'); czEl.classList.remove('lang-active');
        enEl.setAttribute('aria-current', 'true'); czEl.setAttribute('aria-current', 'false');
      }
    }
    document.title = window.t('meta.title');
    var metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', window.t('meta.description'));
    if (typeof window._wizRefreshSummary === 'function') window._wizRefreshSummary();
  };

  // apply on DOMContentLoaded so data-i18n attributes are present
  document.addEventListener('DOMContentLoaded', function () {
    window.applyLanguage(window.currentLang);
  });
